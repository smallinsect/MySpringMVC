package com.cod.bo;

public class DefinitionBO {
	private Double definitionlevel;
	private String definitiondesc;
	private Double terminaltype;
	private Double filesize;
	public Double getDefinitionlevel() {
		return definitionlevel;
	}
	public void setDefinitionlevel(Double definitionlevel) {
		this.definitionlevel = definitionlevel;
	}
	public String getDefinitiondesc() {
		return definitiondesc;
	}
	public void setDefinitiondesc(String definitiondesc) {
		this.definitiondesc = definitiondesc;
	}
	public Double getTerminaltype() {
		return terminaltype;
	}
	public void setTerminaltype(Double terminaltype) {
		this.terminaltype = terminaltype;
	}
	public Double getFilesize() {
		return filesize;
	}
	public void setFilesize(Double filesize) {
		this.filesize = filesize;
	}
	
}
